# Tipos de datos en Python

# Strings
print("Data type")
print("Strings")
print("Concatenando" + "Strings")

# Integers
print(2)
print(type(2))

# Floats
print(2.0 + 4.55)
print(type(2.0))

# Booleans
print(True)
print(type(True))
print(False)
print(type(False))

# Lists
lista = [1, 2, 3]
print(lista)
print(type(lista))

#Tuples
tupla = (1, 2, 3)
print(tupla)
print(type(tupla))

#Dictionaries
diccionario = {"name": "John", "age": 30, "city": "New York"}
print(diccionario)
print(type(diccionario))